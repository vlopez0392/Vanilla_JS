//Switch statement syntax 
const color = "teal"

switch(color){
    case "red":{
        console.log("Color is red");
        break;}
    case "green":{
        console.log("Color is greed");
        break; }
    case "teal":{
        console.log("Teal is the best color!");
        break;}
    default:
        console.log("It's not in our color pallette");
}
